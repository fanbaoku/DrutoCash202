package com.druto.loan.cash.drutocash.entity

class ImageResult {
    var boat: String? = null
    var drop: String? = null
    var baseline: String? = null
    var shelling: String? = null
    var spleen: String? = null
    var saver: Int = 0

    override fun toString(): String {
        return "ImageResult{" +
                "boat='" + boat + '\'' +
                ", drop='" + drop + '\'' +
                ", baseline='" + baseline + '\'' +
                ", shelling='" + shelling + '\'' +
                ", spleen='" + spleen + '\'' +
                ", saver=" + saver +
                '}'
    }
}
